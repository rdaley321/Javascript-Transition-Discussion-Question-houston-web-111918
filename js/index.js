function fade(element, ms){
    var op = 1;  // initial opacity
    var timer = setInterval(function () {
        if (op <= 0.1){
            clearInterval(timer);
            element.style.display = 'none';
        }
        element.style.opacity = op;
        element.style.filter = 'alpha(opacity=' + op * 100 + ")";
        op -= op * 0.1;
    }, ms);
}

function getValue(){
  const input = document.getElementById('interval')
  return input.value
}

const pic = document.getElementById('foreground')
document.getElementById('save_lincoln').addEventListener('click', function(){fade(pic, getValue())}, false)
